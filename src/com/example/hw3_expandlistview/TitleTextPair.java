package com.example.hw3_expandlistview;

public class TitleTextPair {
	
	private String mTitle;
	private String mText;
	
	public TitleTextPair(String title, String text) {
		
		mTitle = title;
		mText = text;
	}
	
	public String getTitle() {
		
		return mTitle;
	}
	
	public String getText() {
		
		return mText;
	}
}
